#include "libft.h"

void	ft_bzero(void *str, size_t n)
{
	if (n != 0)
	{
		while (n != 0)
		{
			((char *)str)[n - 1] = 0;
			n--;
		}
		((char *)str)[0] = 0;
	}
}
